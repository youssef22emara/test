let currentZekrIndex = 0; // الفهرس الحالي للذكر
let currentCount = 0; // العدد الحالي للذكر الحالي

// ============================================
// تهيئة الصفحة عند التحميل
// ============================================
document.addEventListener('DOMContentLoaded', function() {
    // الحصول على عناصر الصفحة
    const zekrText = document.querySelector('.zekr-text');
    const counterButton = document.querySelector('.counter-btn');
    const zekrNumber = document.querySelector('.zekr-number');
    
    // عرض الذكر الأول
    displayZekr();
    
    // إضافة حدث الضغط على الزر
    counterButton.addEventListener('click', function() {
        incrementCounter();
    });
    
    // دالة عرض الذكر الحالي
    function displayZekr() {
        // التحقق من وجود أذكار
        if (azkarData.length === 0) {
            zekrText.textContent = "لا توجد أذكار متاحة";
            counterButton.textContent = "0/0";
            counterButton.disabled = true;
            return;
        }
        
        // التحقق من انتهاء جميع الأذكار
        if (currentZekrIndex >= azkarData.length) {
            zekrText.textContent = "تم إكمال جميع الأذكار! 🎉";
            counterButton.textContent = "مكتمل";
            counterButton.disabled = true;
            counterButton.classList.add('completed');
            zekrNumber.textContent = "";
            return;
        }
        
        // عرض الذكر الحالي
        const currentZekr = azkarData[currentZekrIndex];
        zekrText.textContent = currentZekr.text;
        
        // عرض رقم الذكر الحالي من إجمالي الأذكار (1/16, 2/16, إلخ)
        const totalAzkar = azkarData.length;
        const currentZekrNumber = currentZekrIndex + 1;
        zekrNumber.textContent = `${currentZekrNumber} / ${totalAzkar}`;
        
        // تفعيل الزر عند بدء ذكر جديد
        counterButton.disabled = false;
        counterButton.classList.remove('completed');
        
        // تحديث العداد
        updateCounter();
    }
    
    // ============================================
    // دالة زيادة العداد
    // ============================================
    function incrementCounter() {
        const currentZekr = azkarData[currentZekrIndex];
        
        // التحقق من عدم تجاوز العدد المحدد
        if (currentCount >= currentZekr.count) {
            // إذا تم الوصول للعدد المحدد، لا تزيد
            return;
        }
        
        // زيادة العدد
        currentCount++;
        
        // تحديث العداد في الواجهة
        updateCounter();
        
        // التحقق من إكمال العدد المطلوب
        if (currentCount >= currentZekr.count) {
            // تعطيل الزر عند الوصول للعدد المحدد
            counterButton.disabled = true;
            counterButton.classList.add('completed');
            
            // الانتقال للذكر التالي بعد تأخير قصير
            setTimeout(() => {
                moveToNextZekr();
            }, 800);
        }
    }
    
    // ============================================
    // دالة تحديث العداد في الواجهة
    // ============================================
    function updateCounter() {
        const currentZekr = azkarData[currentZekrIndex];
        counterButton.textContent = `${currentCount}/${currentZekr.count}`;
        
        // إضافة تأثير عند الضغط
        counterButton.classList.add('pressed');
        setTimeout(() => {
            counterButton.classList.remove('pressed');
        }, 200);
    }
    
    // ============================================
    // دالة الانتقال للذكر التالي
    // ============================================
    function moveToNextZekr() {
        // الانتقال للذكر التالي
        currentZekrIndex++;
        currentCount = 0;
        
        // إضافة تأثير انتقال
        const card = document.querySelector('.card');
        card.classList.add('transitioning');
        
        setTimeout(() => {
            displayZekr();
            card.classList.remove('transitioning');
        }, 300);
    }
    
    // ============================================
    // دالة إعادة تعيين (اختيارية)
    // ============================================
    function resetAll() {
        currentZekrIndex = 0;
        currentCount = 0;
        displayZekr();
    }
    
    // تصدير الدوال للاستخدام الخارجي (اختياري)
    window.resetAzkar = resetAll;
});
